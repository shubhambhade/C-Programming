#include<stdio.h>

// single line comments

/*
multiple 
line 
comments
*/
int main()
{
    // integer data type
    int a = 12;
    printf("Integer variable : %d",a);

    // float data type
    float b = 13.34;
    printf("\nFloat variable : %f",b);

    // character data type
    char c = 'a';
    printf("\nCharacter variable : %c",c);
    return 0;
}